# Word Problems
# Demonstrates numbers and math

print("If a 2000 pound pregnant hipoo gives birth to a 100 pound calf,"\
      + " but then eats 50 pounds of food, how much does she weigh?")
input("\n\nPress the enter key to find out.")
print("2000 - 100 + 50 =", 2000-100+50)


print("If an adventurer returns from a successful quest and buys"\
      + " each of 6 companions 3 bottles of ale, how many bottles are purchased?")
input("\n\nPress the enter key to find out.")
print("6 * 3 =", 6*3)


print("If a resturant check comes to 19 dollars with tip, and you and your friends split"\
      + " it evenly 4 ways, how much do you each throw in?")
input("\n\nPress the enter key to find out.")
print("19 / 4 =", 19/4)


print("If a group of 4 pirates finds a chest full of 107 gold coins, and they divide"\
      + " the booty evenly, how many whole coins does each get?")
input("\n\nPress the enter key to find out.")
print("107 // 4 =", 107//4)


print("If the same group of 4 pirates evenly divides the chest full of 107 gold coins,"\
      + " how many coins are left overt?")
input("\n\nPress the enter key to find out.")
print("107 % 4 =", 107%4)


